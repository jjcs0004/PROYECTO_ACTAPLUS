public class Liga {

    private int Id_ligas;
    private String Nombre;
    private int Id_anio;


    public int getId_ligas() {
        return Id_ligas;
    }

    public void setId_ligas(int id_ligas) {
        Id_ligas = id_ligas;
    }

    public String getNombre() {
        return Nombre;
    }

    public void setNombre(String nombre) {
        Nombre = nombre;
    }

    public int getId_anio() {
        return Id_anio;
    }

    public void setId_anio(int id_anio) {
        Id_anio = id_anio;
    }
}



